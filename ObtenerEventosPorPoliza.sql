SELECT PP.NUMEROPOLIZAINPUT AS POLIZA, P.OPERATIONPK AS OPERACION,E2.DESCRIPTION AS EVENTO ,P.INITIALDATE INICIO_VIGENCIA, P.FINISHDATE FIN_VIGENCIA
FROM INTERSEGURO.CONTEXTOPERATION CO
       INNER JOIN INTERSEGURO.POLICYDCO P on CO.ID = P.OPERATIONPK
       INNER JOIN INTERSEGURO.PREPOLICY PP ON PP.PK = P.DCOID
       INNER JOIN INTERSEGURO.EVENTDCO E on E.IDDCOEVENT = P.IDDCOEVENT
       INNER JOIN INTERSEGURO.EVENTDCO E on E.OPERATIONPK = P.OPERATIONPK
       INNER JOIN INTERSEGURO.EVENTTYPE E2 on E.EVENTTYPEID = E2.EVENTTYPEID
WHERE CO.STATUS =2  AND E2.DESCRIPTION ='Renovar'--AND P.INITIALDATE > to_date('01/03/2019','DD/MM/YYYY') 
AND PP.NUMEROPOLIZAINPUT IN 
('900049551',
'5000025-000218978',
'5000007-000270468',
'5000006-000270165',
'900008115',
'900006984',
'900007055',
'900010380',
'900054237',
'900050941',
'900059968')
ORDER BY PP.NUMEROPOLIZAINPUT,P.INITIALDATE, P.FINISHDATE desc
